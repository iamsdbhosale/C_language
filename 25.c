/*C Program to display array elements with address:*/
/*
1D array declaration and displaying element on output screen
*/
#include<stdio.h>
void main()
{
int a[3] = { 11, 22, 33 };
printf("\n a[0] ,value=%d", a[0]);
printf("\n a[1] ,value=%d", a[1]);
printf("\n a[2] ,value=%d", a[2]);
}
/*Output :
a[0] ,value=11
a[1] ,value=22
a[2] ,value=33
*/
